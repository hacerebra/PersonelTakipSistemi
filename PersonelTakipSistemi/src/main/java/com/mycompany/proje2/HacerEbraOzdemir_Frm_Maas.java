    private void btn_maasEkleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_maasEkleActionPerformed

        if (tbl_maas.getSelectedRow() < 0) {//tbl_maas tablosunda herhangi bir satirin secili olup olmadigi kontrol edilir.
            return;
        }

        String maas = (String) tbl_maas_model.getValueAt(tbl_maas.getSelectedRow(), 1);//Secili satirdaki 1. sutundaki deger (maas) alinir ve bir dize olarak maas degiskenine atanir.
        HacerEbraOzdemir_DB_PersonelTakip.PersoneleMaasEkle(this.personel.ad_soyad, maas);//DB_PersonelTakip sinifindan PersoneleMaasEkle methodu cagrilir ve this.personel.ad_soyad ve maas degiskenleri bu methoda parametre olarak verilir. Secilen personele belirli bir maas degerinin eklenmesini saglar.
    }//GEN-LAST:event_btn_maasEkleActionPerformed

