    //Bu method, veritabanindan alinan maas bilgilerini alir ve her bir maas kaydini tabloya ekler. 
    private void SetMaasToTable() {

        ArrayList<HacerEbraOzdemir_Maas> maaslar = HacerEbraOzdemir_DB_PersonelTakip.GetMaas();//"DB_PersonelTakip" sinifindan "GetMaas" methodu cagrilir ve bu yontemden donen maas bilgileri bir ArrayList<Maas> nesnesine atanir. Bu, veritabanindan tüm maas bilgilerini almayi saglar.
        tbl_maas_model.setRowCount(0);//tbl_maas_model nesnesinin satir sayisi sifirlanir. Bu, tablonun mevcut satirlarini temizler.
        for (HacerEbraOzdemir_Maas maas : maaslar) {
            tbl_maas_model.addRow(new Object[]{maas.maas_turu, maas.maas});//Maas turu ve maas degeri tabloya eklenir.

        }
    }

