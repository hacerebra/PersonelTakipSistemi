    private void tbl_personelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_personelMouseClicked

        //Secilen satirin indeksinin kontrolu yapilir. Eger secilen satirin indeksi 0'dan kucukse (yani herhangi bir satir secilmemisse), islem sonlandirilir ve geri dondurur.
        if (tbl_personel.getSelectedRow() < 0) {
            return;
        }
        int id = (int) tbl_model.getValueAt(tbl_personel.getSelectedRow(), 0);//Eger bir satir secilmisse, secilen satirdaki ID degeri alinir. 
        HacerEbraOzdemir_Personel np1 = HacerEbraOzdemir_DB_PersonelTakip.IdIleArama(id);//DB_PersonelTakip.IdIleArama() metodu cagrilir ve ilgili personel bilgileri alinir. 
        //Eger personel bulunmussa, ilgili metin alanlari ve secenekler guncellenir.
        if (np1 != null) {
            txt_id.setText(np1.id + "");
            txt_id.setEnabled(false);
            txt_adSoyad.setText(np1.ad_soyad);
            txt_mail.setText(np1.mail);
            txt_sifre.setText(np1.sifre);
            cbox_fullTime.setSelected(np1.calisma_saati);
            switch (np1.pozisyon) {
                case "Şef":
                    cbox_pozisyon.setSelectedIndex(0);
                    break;
                case "Asistan":
                    cbox_pozisyon.setSelectedIndex(1);
                    break;
                case "Stajyer":
                    cbox_pozisyon.setSelectedIndex(2);
                    break;
            }
            switch (np1.cinsiyet) {
                case "Kadın":
                    cbox_cinsiyet.setSelectedIndex(0);
                    break;
                case "Erkek":
                    cbox_cinsiyet.setSelectedIndex(1);
                    break;
            }
            btn_personelEkle.setEnabled(false);//"Personel Ekle" butonu devre disi birakilir
            //"Personel Guncelle" ve "Personel Sil" butonlari etkinlestirilir.
            btn_personelGuncelle.setEnabled(true);
            btn_personelSil.setEnabled(true);
        }
    }//GEN-LAST:event_tbl_personelMouseClicked

