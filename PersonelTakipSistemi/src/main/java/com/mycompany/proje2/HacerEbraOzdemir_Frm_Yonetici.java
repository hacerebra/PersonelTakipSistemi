    //Bu method, kullanicinin gerekli alanlari doldurarak yeni bir personel eklemesini saglar. Mail adresinin dogrulugunu kontrol eder ve veritabanina personeli ekler.  
    private void btn_personelEkleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_personelEkleActionPerformed

        HacerEbraOzdemir_Personel np1 = new HacerEbraOzdemir_Personel();//Yeni bir "Personel" nesnesi olusturulur ve "np1" degiskenine atanir.
        //Metin kutularındaki ve diğer componentlardaki  biligiler personelin bilgilerine atanir.
        np1.id = Integer.parseInt(txt_id.getText());
        np1.ad_soyad = txt_adSoyad.getText();
        np1.mail = txt_mail.getText();
        np1.sifre = txt_sifre.getText();
        np1.pozisyon = cbox_pozisyon.getSelectedItem().toString();
        np1.calisma_saati = cbox_fullTime.isSelected();
        np1.cinsiyet = cbox_cinsiyet.getSelectedItem().toString();
        try {
            Pattern pattern = Pattern.compile("^([a-z]+\\w*){4,}@{1}((gmail)|(hotmail)){1}(.com){1}$");//Mail icin regex olusturulur ve mail formatının dogrulugunu kontrol etmek icin kullanilir.
            Matcher matcher = pattern.matcher(txt_mail.getText());
            if (matcher.find()) {//Mail gecerliyse;
                HacerEbraOzdemir_DB_PersonelTakip.PersonelEkle(np1);//"np1" nesnesi veritabanina eklenir.
                JOptionPane.showMessageDialog(rootPane, "Personel Eklendi");//Personelin eklendigine dair mesaj gosterilir.
                tbl_model.addRow(new Object[]{np1.id, np1.ad_soyad, np1.pozisyon, np1.mail});//Tablo modeline yeni bir satir eklenir ve personelin bilgileri tabloya eklenir.
                personels.add(np1);//personels arraylistine yeni bir personel eklenir.
            } else {
                JOptionPane.showMessageDialog(rootPane, "Geçersiz mail! Tekrar deneyiniz.");//Gecerli bir mail adresi degilse, hata mesajlari goruntulenir ve personel eklenemez.
                JOptionPane.showMessageDialog(rootPane, "Personel Eklenemedi");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());//Herhangi bir hata durumunda, hata mesaji gonderilir.
        }
    }//GEN-LAST:event_btn_personelEkleActionPerformed

